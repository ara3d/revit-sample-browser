using System.Drawing;
using System.Windows;
using System.Windows.Interop;
using System.Windows.Media.Imaging;
using Autodesk.Revit.Attributes;
using Autodesk.Revit.UI;
using Revit.SDK.Samples.PostCommandWorkflow.CS.Properties;
using Size = System.Drawing.Size;

namespace Revit.SDK.Samples.PostCommandWorkflow.CS
{
    /// <summary>
    ///     Implements the Revit add-in interface IExternalApplication
    /// </summary>
    [Transaction(TransactionMode.Manual)]
    [Regeneration(RegenerationOption.Manual)]
    public class Application : IExternalApplication
    {
        /// <summary>
        ///     The path to this add-in assembly.
        /// </summary>
        private static readonly string addAssemblyPath = typeof(Application).Assembly.Location;

        public Result OnShutdown(UIControlledApplication application)
        {
            return Result.Succeeded;
        }

        public Result OnStartup(UIControlledApplication application)
        {
            CreateUIPanel(application);
            return Result.Succeeded;
        }

        /// <summary>
        ///     Creates the ribbon panel for this add-in.
        /// </summary>
        /// <param name="application">The application.</param>
        private void CreateUIPanel(UIControlledApplication application)
        {
            var rp = application.CreateRibbonPanel("UI");
            var setupMonitor = new PushButtonData("Setup_Revision_Monitor", "Setup Revision Monitor",
                addAssemblyPath,
                typeof(PostCommandRevisionMonitorCommand).FullName);
            var setupMonitorPB = rp.AddItem(setupMonitor) as PushButton;

            SetIconsForPushButton(setupMonitorPB, Resources.RevisionIcon);

            // Pass the pushbutton handle to the command class, so that the text can be changed when pressed
            PostCommandRevisionMonitorCommand.SetPushButton(setupMonitorPB);
        }

        /// <summary>
        ///     Utility for adding icons to the button.
        /// </summary>
        /// <param name="button">The push button.</param>
        /// <param name="icon">The icon.</param>
        private static void SetIconsForPushButton(PushButton button, Icon icon)
        {
            button.LargeImage = GetStdIcon(icon);
            button.Image = GetSmallIcon(icon);
        }

        /// <summary>
        ///     Gets the standard sized icon as a BitmapSource.
        /// </summary>
        /// <param name="icon">The icon.</param>
        /// <returns>The BitmapSource.</returns>
        private static BitmapSource GetStdIcon(Icon icon)
        {
            return Imaging.CreateBitmapSourceFromHIcon(
                icon.Handle,
                Int32Rect.Empty,
                BitmapSizeOptions.FromEmptyOptions());
        }

        /// <summary>
        ///     Gets the small sized icon as a BitmapSource.
        /// </summary>
        /// <param name="icon">The icon.</param>
        /// <returns>The BitmapSource.</returns>
        private static BitmapSource GetSmallIcon(Icon icon)
        {
            var smallIcon = new Icon(icon, new Size(16, 16));
            return Imaging.CreateBitmapSourceFromHIcon(
                smallIcon.Handle,
                Int32Rect.Empty,
                BitmapSizeOptions.FromEmptyOptions());
        }
    }
}
